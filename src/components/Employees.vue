  <div class="container mt-3">
    <div class="row">
      <div class="col">
        <p class="h3 text-success">Employees</p>
        <p class="fst-italic">
          Lorem ipsum dolor sit amet, consectetur adipisicing elit. Ad dicta
          dolores, excepturi harum impedit maiores similique sint soluta vel?
          Asperiores consectetur dolores fuga fugit iste labore laborum quos
          unde voluptate.
        </p>
      </div>
    </div>
  </div>

  <div class="container">
    <div class="row">
      <div class="col-md-6">
        <ul class="list-group">
          <li
            v-for="employee of employeesState.employees"
            :key="employee.id"
            class="list-group-item list-group-item-success"
            @change="updateSelected(employee.id)"
          >
            <input
              :checked="employee.isSelected"
              class="form-check-input"
              type="checkbox"
            />
            {{ employee.name }}
          </li>
        </ul>
      </div>
      <div class="col-md-6">
